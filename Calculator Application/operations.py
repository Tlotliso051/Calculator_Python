def add(first_value,second_value):
    """
    Function adds the operands and return the sum.
    """
    print(f"The total value is: {first_value + second_value}")


def divide(first_value,second_value):
    """
    Function divides the operands and return the sum.
    """
    print(f"The total value is: {first_value / second_value}")


def multiply(first_value,second_value):
    """
    Function multiply the operands and return the sum.
    """
    print(f"The product value is: {first_value * second_value}")


def subtract(first_value,second_value):
    """
    Function subtract and return the value
    """
    print(f"The total value is: {first_value - second_value}")





    



